interface SidebarProps {
  collapsed: boolean;
  onToggle: () => void;
}

export function Sidebar({ collapsed, onToggle }: SidebarProps) {
  const logout = useAuthStore((s) => s.logout);
  const navigate = useNavigate();

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  return (
    <aside className={`sidebar${collapsed ? ' sidebar--collapsed' : ''}`}>
      <div className="sidebar__logo">
        <div className="sidebar__logo-icon">
          <svg width="22" height="22" viewBox="0 0 24 24" fill="none" stroke="#1a1a1a" strokeWidth="2.2" strokeLinecap="round" strokeLinejoin="round">
            <path d="M22 10v6M2 10l10-5 10 5-10 5z" />
            <path d="M6 12v5c3 3 9 3 12 0v-5" />
          </svg>
        </div>
        {!collapsed && (
          <div className="sidebar__logo-texts">
            <span className="sidebar__logo-text">Driving School</span>
            <span className="sidebar__logo-sub">Quản Lý Luyện Thi</span>
          </div>
        )}
        <button className="sidebar__close" onClick={onToggle} aria-label="Toggle sidebar">
          {collapsed ? <IconMenu /> : '×'}
        </button>
      </div>

      <nav className="sidebar__nav">
        {NAV_ITEMS.map((item) => (
          <NavLink
            key={item.path}
            to={item.path}
            end={item.path === '/dashboard'}
            title={collapsed ? item.label : undefined}
            className={({ isActive }) =>
              `sidebar__nav-item${isActive ? ' sidebar__nav-item--active' : ''}`
            }
          >
            <span className="sidebar__nav-icon">{item.icon}</span>
            {!collapsed && item.label}
          </NavLink>
        ))}
      </nav>

      <div className="sidebar__footer">
        <button
          className="sidebar__logout"
          onClick={handleLogout}
          title={collapsed ? 'Đăng Xuất' : undefined}
        >
          <span className="sidebar__nav-icon"><IconLogout /></span>
          {!collapsed && 'Đăng Xuất'}
        </button>
      </div>
    </aside>
  );
}
